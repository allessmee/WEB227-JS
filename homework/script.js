let num;
let a = prompt("Введите стоимость покупки: ");
if (a >= 500 && a < 1000){
    num = (a - a * 0.03).toFixed(2);
    alert("Стоимость покупки: " + a +" рублей "+ "\nСкидка: " +  "3%" + "\nК оплате: " + num + " рублей ")
}
else if (a >= 1000){
    num = parseInt(a - a * 0.05)
    alert("Стоимость покупки: " + a +" рублей "+ "\nСкидка: " + "5%" + "\nК оплате: " + num + " рублей ")
}
else if(a < 500){
    alert ("Скидка не распространяется")
}